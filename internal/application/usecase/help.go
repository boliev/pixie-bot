package usecase

const helpMessage = `🖼 Как пользоваться ботом:

1. Отправьте одно или несколько фото (до 10 штук) с текстовым описанием в caption.
2. Бот отредактирует фото с помощью AI и вернёт результат.

💡 Подсказки:
• Одно фото — отправьте его с описанием в caption.
• Несколько фото — отправьте альбомом с описанием в caption к первому фото.
• Если отправить фото без описания, бот попросит добавить его.

💰 Стоимость:
• 1 кредит за одну генерацию (неважно, 1 или 10 фото).
• Новым пользователям начисляется 3 бесплатных кредита.

📦 Лимиты:
• Максимум 10 фото за один запрос.

💳 Как пополнить баланс:
• Нажмите кнопку «Пополнить» и выберите нужный пакет кредитов.
• Оплата через Telegram Stars (⭐).`

type HelpUseCase struct{}

func NewHelpUseCase() *HelpUseCase {
	return &HelpUseCase{}
}

func (uc *HelpUseCase) GetHelpMessage() string {
	return helpMessage
}
